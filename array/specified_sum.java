import java.util.*;
public class specified_sum
{
 	public static void main(String[] args)
 	{
 	 int i,j;
 	 int a[]={1,2,3,4,5,6,7,8};
 	 int l=a.length;
 	 int val=14;
 	 int c=0;
 	 
 	 	for(i=0;i<l-1;i++)
 	 		{
 	 		 for(j=i+1;j<l;j++)
 	 		 	{
 	 		           if(a[i]+a[j]==val)
 	 		           {
 	 		           
 	 		           c=1;
 	 		         
                                    }
                                
                                  }
                            
                            }
                            if(c==1)
                            {
                            	System.out.println("the specified value is present");
                            	
                            }
                            else
                                {
                                 System.out.println("the specified value is not  present ");
                                 }
                            
  
  }
  
  }
	
